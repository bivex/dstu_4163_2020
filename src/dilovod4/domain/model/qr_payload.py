"""Формування корисного навантаження QR-коду за §5.10 ДСТУ 4163:2020.

Крос-лінк (заголовок dstu-файлу): QR (§5.10) несе дані про КЕП/печатку +
кваліфіковані позначки часу. Це чиста доменна логіка (без IO, без рендерингу).

ВАЖЛИВО про щільність: фізичний розмір QR жорстко 21×21 мм (§5.10), збільшити
не можна. Тому навантаження тримаємо компактним і в ASCII — кирилиця в UTF-8
по 2 байти/символ роздуває версію QR, модуль стає <0.4 мм і камера телефона не
зчитує. Підписувач та видавець і так наведені у видимій текстовій відмітці КЕП,
тож у QR кладемо лише машинні поля для верифікації (серійник, дати, час, статус).

Формат (версія 2) — позиційний, поля через ';', ASCII:
    DSTU4163;<v>;<typ>;<sn>;<vf>;<vt>;<ts>;<st>
Префікс 'DSTU4163' — маркер схеми для верифікатора.
"""

from __future__ import annotations

from .signature import ElectronicSignatureMark

# Версія схеми навантаження — для зворотної сумісності верифікаторів.
QR_PAYLOAD_VERSION = "2"
QR_PAYLOAD_PREFIX = "DSTU4163"


def build_signature_qr_payload(mark: ElectronicSignatureMark) -> str:
    """Зібрати компактний ASCII-рядок навантаження QR з даних підпису.

    Позиційні поля (§5.10: дані про КЕП/печатку + позначка часу):
      [0] DSTU4163 — маркер схеми
      [1] версія схеми
      [2] тип підпису: QES (кваліфікований) / AES (удосконалений)
      [3] серійний номер сертифіката
      [4] чинний від
      [5] чинний до
      [6] кваліфікована позначка часу
      [7] статус сертифіката за Art.24: V (valid) / X (invalid)

    Підписувач та видавець навмисно НЕ кодуються (вони у видимій відмітці) —
    це тримає QR у читабельній щільності за фіксованих 21 мм.
    """
    typ = "QES" if mark.is_qualified else "AES"
    status = "V" if mark.certificate_valid else "X"
    fields = [
        QR_PAYLOAD_PREFIX,
        QR_PAYLOAD_VERSION,
        typ,
        mark.certificate_serial,
        mark.valid_from,
        mark.valid_to,
        mark.timestamp,
        status,
    ]
    return ";".join(_sanitize(f) for f in fields)


def _sanitize(value: str) -> str:
    """Прибрати роздільник зі значень (позиційний формат не екранує)."""
    return value.replace(";", " ")
