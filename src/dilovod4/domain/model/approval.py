"""Реквізити погодження та затвердження — value objects.

ДСТУ 4163:2020, §4.4:
  • реквізит 21 — гриф затвердження (ЗАТВЕРДЖУЮ / ЗАТВЕРДЖЕНО);
  • реквізит 23 — гриф погодження (ПОГОДЖЕНО) — ЗОВНІШНЄ погодження зі
    сторонньою організацією;
  • реквізит 24 — віза — ВНУТРІШНЄ погодження (юрист, головбух, керівник
    підрозділу) у межах самої організації.

Кожен реквізит має дві форми: персональну (посадова особа підписує) або
через документ (посилання на протокол/наказ, яким погоджено/затверджено).
Це чисті value objects — без IO та рендерингу.
"""

from __future__ import annotations

from dataclasses import dataclass

from ..errors import InvariantViolation


@dataclass(frozen=True)
class ApprovalGrant:
    """Гриф затвердження (реквізит 21) — ЗАТВЕРДЖУЮ / ЗАТВЕРДЖЕНО.

    Персональна форма (ЗАТВЕРДЖУЮ): посада, розшифрування, дата.
    Документальна форма (ЗАТВЕРДЖЕНО): посилання на документ (вид, дата, №).
    Розташування — праворуч угорі, відступ 100 мм від лівого поля (§7.7).
    """

    position: str = ""  # посада особи, що затверджує (персональна форма)
    name: str = ""  # власне ім'я ПРІЗВИЩЕ
    date: str = ""  # дата затвердження
    document_reference: str = ""  # «Наказ ... від ... № ...» (документальна форма)

    def __post_init__(self) -> None:
        if not self.is_by_document and not self.position.strip():
            raise InvariantViolation(
                "гриф затвердження: потрібні посада/розшифрування або посилання на документ"
            )

    @property
    def is_by_document(self) -> bool:
        return bool(self.document_reference.strip())

    @property
    def heading(self) -> str:
        return "ЗАТВЕРДЖЕНО" if self.is_by_document else "ЗАТВЕРДЖУЮ"


@dataclass(frozen=True)
class Agreement:
    """Гриф погодження (реквізит 23) — ПОГОДЖЕНО, ЗОВНІШНЄ погодження.

    Персональна форма: посада + найменування сторонньої організації,
    розшифрування, дата. Документальна форма: посилання на протокол/лист.
    Розташування — нижче тексту/підпису, від лівого поля (§7.7, 100 мм).
    """

    position: str = ""  # посада + організація (персональна форма)
    name: str = ""  # власне ім'я ПРІЗВИЩЕ
    date: str = ""  # дата погодження
    document_reference: str = ""  # «Протокол ... від ... № ...» (документальна форма)

    def __post_init__(self) -> None:
        if not self.is_by_document and not self.position.strip():
            raise InvariantViolation(
                "гриф погодження: потрібні посада/розшифрування або посилання на документ"
            )

    @property
    def is_by_document(self) -> bool:
        return bool(self.document_reference.strip())


@dataclass(frozen=True)
class Visa:
    """Віза (реквізит 24) — ВНУТРІШНЄ погодження у межах організації.

    Посада, розшифрування, дата; за потреби — застереження (зауваження
    візувальника). Розташування — нижче підпису на лицьовому боці останнього
    аркуша (для внутрішніх) або на копії/звороті (для вихідних).
    """

    position: str  # посада візувальника (юрист, головбух тощо)
    name: str  # власне ім'я ПРІЗВИЩЕ
    date: str = ""  # дата візування
    remark: str = ""  # зауваження до документа (за наявності)

    def __post_init__(self) -> None:
        if not self.position.strip():
            raise InvariantViolation("віза: посада не може бути порожньою")
        if not self.name.strip():
            raise InvariantViolation("віза: розшифрування не може бути порожнім")
